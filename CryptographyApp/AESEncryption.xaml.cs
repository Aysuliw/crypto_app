        public uint[] subWord(uint[] word)
        {
            uint[] result = new uint[4];
            result[0] = Sbox[word[0] >> 4, word[0] & 0x0f];
            result[1] = Sbox[word[1] >> 4, word[1] & 0x0f];
            result[2] = Sbox[word[2] >> 4, word[2] & 0x0f];
            result[3] = Sbox[word[3] >> 4, word[3] & 0x0f];
            return result;

        }
        public uint[] rotateWord(uint[] word)
        {
            uint[] result = new uint[4];
            result[0] = word[1];
            result[1] = word[2];
            result[2] = word[3];
            result[3] = word[0];
            return result;

        }
        public void keyGenerator()
        {
            for (int i = 0; i < 4; i++)
            {

                for (int j = 0; j < 4; j++)
                {
                    w[i, j] = key[j, i];
                }

            }
            uint[] temp = new uint[4];
            for (int i = 4; i < 44; i++)
            {
                temp[0] = w[i - 1, 0];
                temp[1] = w[i - 1, 1];
                temp[2] = w[i - 1, 2];
                temp[3] = w[i - 1, 3];
                if (i % 4 == 0)
                {
                    temp = subWord(rotateWord(temp));
                    temp[0] = temp[0] ^ Rcon[i / 4, 0];
                    temp[1] = temp[1] ^ Rcon[i / 4, 1];
                    temp[2] = temp[2] ^ Rcon[i / 4, 2];
                    temp[3] = temp[3] ^ Rcon[i / 4, 3];

                }
                w[i, 0] = w[i - 4, 0] ^ temp[0];
                w[i, 1] = w[i - 4, 1] ^ temp[1];
                w[i, 2] = w[i - 4, 2] ^ temp[2];
                w[i, 3] = w[i - 4, 3] ^ temp[3];
            }

            for(int i = 0; i < 11; i++)
            {
                uint[,] K = new uint[4, 4];
                for (int j = 0; j < 4; j++)
                {
                    K[0, j] = w[i * 4 + j, 0];
                    K[1, j] = w[i * 4 + j, 1];
                    K[2, j] = w[i * 4 + j, 2];
                    
                    K[3, j] = w[i * 4 + j, 3];
                }
                roundKeys.Insert(i,K);
                
            }

        }
        public void AddRoundKey(int round)
        {
            uint[,] tmpMatrix = new uint[4, 4];
            uint[,] P = roundKeys[round];
            for (int i = 0; i < 4; i++)
            {
                for (int j = 0; j < 4; j++)
                {
                    tmpMatrix[i, j] = state[i, j] ^ P[i, j];
                }
            }
            state = tmpMatrix;
            listState.Add(tmpMatrix);
        }
        public void ShiftRows()
        {
            uint[,] tmpMatrix = new uint[4, 4];
            uint[,] shiftTemp = new uint[4, 4];
            for (int i = 0; i < 4; i++)
            {
                for (int j = 0; j < 4; j++)
                {
                    shiftTemp[i, j] = state[i, j];
                }
            }

            for (int i = 0; i < 4; i++)
            {
                for (int j = 0; j < 4; j++)
                {
                    tmpMatrix[i, j] = shiftTemp[i, (i + j) % 4];
                }
            }
            state = tmpMatrix;
            listState.Add(tmpMatrix);
        }
        public void SubBytes()
        {
            uint[,] tmpMatrix = new uint[4, 4];
            for (int i = 0; i < 4; i++)
            {
                for (int j = 0; j < 4; j++)
                {
                    tmpMatrix[i, j] = Sbox[(state[i, j] >> 4) % 16, (state[i, j] & 0x0f)];
                }
            }
            state = tmpMatrix;
            listState.Add(tmpMatrix);
        }
        public uint GMul(uint a, uint b)
        {
            uint p = 0;
            for (int i = 0; i < 8; i++)
            {
                if ((b & 1) != 0)
                {
                    p ^= a;
                }
                bool hi_bit_set = (a & 0x80) != 0;
                a <<= 1;
                if (hi_bit_set)
                {
                    a ^= 0x1B;
                }
                b >>= 1;
            }
            return p;
        }
       
        public void MixColumns()
        {
            uint[,] tmpMatrix = new uint[4, 4];
            uint[,] mixTemp = new uint[4, 4];
            for (int i = 0; i < 4; i++)
            {
                for (int j = 0; j < 4; j++)
                {
                    mixTemp[i, j] = state[i, j];
                }
            }
            for (int i = 0; i < 4; i++)
            {

                tmpMatrix[0, i] = (uint)(GMul2[mixTemp[0, i]] ^ GMul3[mixTemp[1, i]] ^ mixTemp[2, i] ^ mixTemp[3, i]);
                
                tmpMatrix[1, i] = (uint)(mixTemp[0, i] ^ GMul2[mixTemp[1, i]] ^ GMul3[ mixTemp[2, i]] ^ mixTemp[3, i]);
                tmpMatrix[2, i] = (uint)(mixTemp[0, i] ^ mixTemp[1, i] ^ GMul2[mixTemp[2, i]] ^ GMul3[mixTemp[3, i]]);
                
                tmpMatrix[3, i] = (uint)(GMul3[mixTemp[0, i]] ^ mixTemp[1, i] ^ mixTemp[2, i] ^ GMul2[mixTemp[3, i]]);
                
            }
            state = tmpMatrix;
            listState.Add(tmpMatrix);
        }
        
